uint8_t linNMCfg_istonormal(void)
{
	uint8_t mode;
	mode = autosar_dirNm_currMode(0);
	if((NM_IMME_REPEAT_MODE == mode) 	||  \
	   (NM_NORMAL_REPEAT_MODE == mode)	||	\
	   (NM_NORMAL_OPER_MODE == mode)	||	\
	   (NM_READY_SLEEP_MODE == mode)		\
	   )
	{
		return 1;
	}

	return 0;
}

uint8_t linNMCfg_istosleep(void)
{
	uint8_t mode;
	mode = autosar_dirNm_currMode(0);
	if((NM_BAT_ON_MODE == mode) 	||  \
	   (NM_SLEEP_MODE == mode)		||	\
	   (NM_PREPARE_SLEEP_MODE == mode)	\
	   )
	{
		return 1;
	}

	return 0;
}

